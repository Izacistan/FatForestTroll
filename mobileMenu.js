function onClickMenu() {
  // Setup 'change' class for 'menu' (Mobile Menu)
  document.getElementById("menu").classList.toggle("change");
  // Setup 'change' class for 'mainNav' (Mobile Menu)
  document.getElementById("mainNav").classList.toggle("change");
  // Setup 'changeBackground' class for 'menuBackground' (Mobile Menu)
  document.getElementById("menuBackground").classList.toggle("changeBackground");
}
